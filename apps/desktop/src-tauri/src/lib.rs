#[cfg(not(target_os = "ios"))]
fn recent_menu_label(title: &str) -> String {
    const MAX_CHARS: usize = 52;
    if title.chars().count() <= MAX_CHARS {
        return title.to_string();
    }

    let mut label = title.chars().take(MAX_CHARS - 1).collect::<String>();
    label.push_str("...");
    label
}

