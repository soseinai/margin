#[cfg(target_os = "macos")]
fn margin_cli_destination() -> Result<PathBuf, String> {
    let destination = margin_cli_destination_candidate()?;

    if destination.exists() {
        if existing_margin_cli(&destination)? {
            ensure_writable_destination(&destination)?;
            return Ok(destination);
        }

        return Err(format!(
            "A different command already exists at {}. Move or rename that command before installing Margin's CLI.",
            destination.display()
        ));
    }

    ensure_writable_destination(&destination)?;

    Ok(destination)
}

#[cfg(target_os = "macos")]
fn margin_cli_destination_candidate() -> Result<PathBuf, String> {
    env::var_os("HOME")
        .map(|home| {
            PathBuf::from(home)
                .join(".local")
                .join("bin")
                .join(CLI_NAME)
        })
        .ok_or_else(|| {
            "Margin could not locate your home directory to install ~/.local/bin/margin."
                .to_string()
        })
}

#[cfg(target_os = "macos")]
fn existing_margin_cli(path: &Path) -> Result<bool, String> {
    if !path.is_file() {
        return Ok(false);
    }

    let contents = fs::read(path).map_err(|err| {
        format!(
            "Unable to inspect existing command at {}: {err}",
            path.display()
        )
    })?;

    Ok(contents
        .windows(CLI_MARKER.len())
        .any(|window| window == CLI_MARKER.as_bytes()))
}

#[cfg(target_os = "macos")]
fn ensure_writable_destination(path: &Path) -> Result<(), String> {
    let parent = path.parent().ok_or_else(|| {
        format!(
            "Unable to locate the parent directory for {}",
            path.display()
        )
    })?;

    fs::create_dir_all(parent)
        .map_err(|err| format!("Unable to create {}: {err}", parent.display()))?;

    let probe_path = parent.join(format!(".margin-cli-install-probe-{}", std::process::id()));

    match fs::OpenOptions::new()
        .write(true)
        .create_new(true)
        .open(&probe_path)
    {
        Ok(_) => {
            let _ = fs::remove_file(&probe_path);
            Ok(())
        }
        Err(err) => Err(format!("Unable to write to {}: {err}", parent.display())),
    }
}

#[cfg(target_os = "macos")]
fn write_executable_file(path: &Path, contents: &str) -> Result<(), String> {
    use std::os::unix::fs::PermissionsExt;

    let parent = path.parent().ok_or_else(|| {
        format!(
            "Unable to locate the parent directory for {}",
            path.display()
        )
    })?;
    let temporary_path = parent.join(format!(".margin-cli-install-{}", std::process::id()));

    fs::write(&temporary_path, contents)
        .map_err(|err| format!("Unable to write {}: {err}", temporary_path.display()))?;
    fs::set_permissions(&temporary_path, fs::Permissions::from_mode(0o755)).map_err(|err| {
        format!(
            "Unable to make {} executable: {err}",
            temporary_path.display()
        )
    })?;
    if let Err(err) = fs::rename(&temporary_path, path) {
        let _ = fs::remove_file(&temporary_path);

        return Err(format!("Unable to install {}: {err}", path.display()));
    }

    Ok(())
}

#[cfg(target_os = "macos")]
fn margin_cli_script(app_bundle_path: Option<&Path>) -> String {
    let app_bundle = app_bundle_path
        .map(shell_single_quoted)
        .unwrap_or_else(|| "''".to_string());

    format!(
        r#"#!/bin/zsh
{CLI_MARKER}
set -e

app_bundle={app_bundle}

args=()
for arg in "$@"; do
  case "$arg" in
    margin://*|file://*|/*)
      args+=("$arg")
      ;;
    *)
      args+=("$PWD/$arg")
      ;;
  esac
done

if [[ -n "$app_bundle" && -d "$app_bundle" ]]; then
  exec open -n "$app_bundle" --args "${{args[@]}}"
fi

exec open -n -a "Margin" --args "${{args[@]}}"
"#
    )
}

#[cfg(target_os = "macos")]
fn shell_single_quoted(value: &Path) -> String {
    let value = value.to_string_lossy();
    format!("'{}'", value.replace('\'', "'\\''"))
}

