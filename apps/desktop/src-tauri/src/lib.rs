#[cfg(target_os = "linux")]
fn linux_app_state_dir(app: &AppHandle) -> Result<PathBuf, String> {
    if let Some(path) = env::var_os("XDG_STATE_HOME").map(PathBuf::from) {
        if path.is_absolute() {
            return Ok(path.join(&app.config().identifier));
        }
    }

    env::var_os("HOME")
        .map(PathBuf::from)
        .map(|home| {
            home.join(".local")
                .join("state")
                .join(&app.config().identifier)
        })
        .ok_or_else(|| "Unable to locate home directory for app state".to_string())
}

#[cfg(not(target_os = "ios"))]
fn run_sosein_oauth_loopback(server_url: String) -> Result<SoseinOAuthSession, String> {
    let api_base = normalized_sosein_api_base(&server_url)?;
    let listener = TcpListener::bind(("127.0.0.1", 0))
        .map_err(|err| format!("Unable to start Sosein Cloud login listener: {err}"))?;
    let port = listener
        .local_addr()
        .map_err(|err| format!("Unable to read Sosein Cloud login listener address: {err}"))?
        .port();
    let callback_url = format!("http://127.0.0.1:{port}/oauth/callback");
    let login_url = format!(
        "{api_base}/v1/auth/oauth/google/login?return_to={}",
        percent_encode_query_value(&callback_url)
    );

    listener
        .set_nonblocking(true)
        .map_err(|err| format!("Unable to configure Sosein Cloud login listener: {err}"))?;
    open_system_browser(&login_url)?;
    let handoff_code = wait_for_sosein_oauth_callback(listener)?;

    exchange_sosein_oauth_handoff(&api_base, &handoff_code)
}

#[cfg(not(target_os = "ios"))]
fn wait_for_sosein_oauth_callback(listener: TcpListener) -> Result<String, String> {
    let deadline = Instant::now() + SOSEIN_OAUTH_TIMEOUT;

    loop {
        match listener.accept() {
            Ok((stream, _address)) => {
                if let Some(handoff_code) = handle_sosein_oauth_connection(stream)? {
                    return Ok(handoff_code);
                }
            }
            Err(err) if err.kind() == std::io::ErrorKind::WouldBlock => {
                if Instant::now() >= deadline {
                    return Err("Timed out waiting for Sosein Cloud login.".to_string());
                }

                thread::sleep(Duration::from_millis(50));
            }
            Err(err) => {
                return Err(format!(
                    "Unable to accept Sosein Cloud login callback: {err}"
                ))
            }
        }
    }
}

#[cfg(not(target_os = "ios"))]
fn handle_sosein_oauth_connection(mut stream: TcpStream) -> Result<Option<String>, String> {
    stream
        .set_read_timeout(Some(Duration::from_secs(3)))
        .map_err(|err| format!("Unable to configure Sosein Cloud login callback: {err}"))?;

    let mut buffer = [0_u8; 8192];
    let read = stream
        .read(&mut buffer)
        .map_err(|err| format!("Unable to read Sosein Cloud login callback: {err}"))?;
    let request = String::from_utf8_lossy(&buffer[..read]);
    let request_line = request.lines().next().unwrap_or("");
    let mut request_parts = request_line.split_whitespace();
    let method = request_parts.next().unwrap_or("");
    let target = request_parts.next().unwrap_or("");

    if method != "GET" {
        write_oauth_http_response(
            &mut stream,
            "405 Method Not Allowed",
            "Margin can only receive Sosein Cloud OAuth GET callbacks.",
        )?;

        return Ok(None);
    }

    if !sosein_oauth_callback_path_matches(target) {
        write_oauth_http_response(
            &mut stream,
            "404 Not Found",
            "Unknown Margin OAuth callback.",
        )?;

        return Ok(None);
    }

    if let Some(error) = oauth_query_value(target, "error")? {
        write_oauth_http_response(
            &mut stream,
            "400 Bad Request",
            "Sosein Cloud login did not complete.",
        )?;

        return Err(format!("Sosein Cloud login failed: {error}"));
    }

    let Some(handoff_code) = oauth_query_value(target, "handoff_code")? else {
        write_oauth_http_response(
            &mut stream,
            "400 Bad Request",
            "Sosein Cloud login callback did not include a handoff code.",
        )?;

        return Err("Sosein Cloud login callback did not include a handoff code.".to_string());
    };

    write_oauth_http_response(
        &mut stream,
        "200 OK",
        "Sosein Cloud login finished. You can return to Margin.",
    )?;

    Ok(Some(handoff_code))
}

#[cfg(not(target_os = "ios"))]
fn write_oauth_http_response(
    stream: &mut TcpStream,
    status: &str,
    message: &str,
) -> Result<(), String> {
    let body = format!(
        "<!doctype html><meta charset=\"utf-8\"><title>Margin</title><body><p>{}</p></body>",
        html_escape(message)
    );
    let response = format!(
        "HTTP/1.1 {status}\r\ncontent-type: text/html; charset=utf-8\r\ncontent-length: {}\r\nconnection: close\r\n\r\n{body}",
        body.len()
    );

    stream
        .write_all(response.as_bytes())
        .map_err(|err| format!("Unable to write Sosein Cloud login response: {err}"))?;
    let _ = stream.flush();

    Ok(())
}

#[cfg(not(target_os = "ios"))]
fn normalized_sosein_api_base(server_url: &str) -> Result<String, String> {
    let trimmed = server_url.trim().trim_end_matches('/');

    if trimmed.starts_with("https://") || trimmed.starts_with("http://") {
        Ok(trimmed.to_string())
    } else {
        Err("Sosein Cloud server URL must start with http:// or https://.".to_string())
    }
}

#[cfg(not(target_os = "ios"))]
fn sosein_oauth_callback_path_matches(target: &str) -> bool {
    target.split_once('?').map_or(target, |(path, _query)| path) == "/oauth/callback"
}

#[cfg(not(target_os = "ios"))]
fn exchange_sosein_oauth_handoff(
    api_base: &str,
    handoff_code: &str,
) -> Result<SoseinOAuthSession, String> {
    let client = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(30))
        .build()
        .map_err(|err| format!("Unable to prepare Sosein Cloud login exchange: {err}"))?;
    let response = client
        .post(sosein_api_url(api_base, "/v1/auth/oauth/handoff/exchange")?)
        .json(&SoseinOAuthHandoffExchangeRequest { handoff_code })
        .send()
        .map_err(|err| format!("Unable to exchange Sosein Cloud login handoff: {err}"))?;
    let status = response.status();
    let body_text = response
        .text()
        .map_err(|err| format!("Unable to read Sosein Cloud login exchange response: {err}"))?;

    if !status.is_success() {
        return Err(format!(
            "Sosein Cloud login exchange failed with {}{}",
            status.as_u16(),
            compact_response_suffix(&body_text)
        ));
    }

    serde_json::from_str(&body_text)
        .map_err(|err| format!("Unable to parse Sosein Cloud login exchange response: {err}"))
}

#[cfg(not(target_os = "ios"))]
fn run_sosein_api_request(
    server_url: String,
    method: String,
    path: String,
    session_token: Option<String>,
    body: Option<serde_json::Value>,
) -> Result<NativeSoseinApiResponse, String> {
    let api_base = normalized_sosein_api_base(&server_url)?;
    let path = normalized_sosein_api_path(&path)?;
    let client = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(30))
        .build()
        .map_err(|err| format!("Unable to prepare Sosein Cloud request: {err}"))?;
    let method = method.trim().to_ascii_uppercase();
    let mut request = match method.as_str() {
        "GET" => client.get(sosein_api_url(&api_base, &path)?),
        "POST" => client.post(sosein_api_url(&api_base, &path)?),
        _ => return Err("Sosein Cloud request method must be GET or POST.".to_string()),
    };

    if let Some(token) = session_token.filter(|token| !token.trim().is_empty()) {
        request = request.bearer_auth(token);
    }

    if let Some(body) = body {
        request = request.json(&body);
    }

    let response = request
        .send()
        .map_err(|err| format!("Unable to send Sosein Cloud request: {err}"))?;
    let status = response.status().as_u16();
    let body_text = response
        .text()
        .map_err(|err| format!("Unable to read Sosein Cloud response: {err}"))?;
    let body = if body_text.trim().is_empty() {
        None
    } else {
        serde_json::from_str(&body_text).ok()
    };

    Ok(NativeSoseinApiResponse {
        status,
        body,
        body_text,
    })
}

#[cfg(not(target_os = "ios"))]
fn normalized_sosein_api_path(path: &str) -> Result<String, String> {
    let trimmed = path.trim();

    if !trimmed.starts_with("/v1/")
        || trimmed.contains("://")
        || trimmed
            .chars()
            .any(|character| character == '\r' || character == '\n')
    {
        return Err("Sosein Cloud request path must be a /v1/ API path.".to_string());
    }

    Ok(trimmed.to_string())
}

#[cfg(not(target_os = "ios"))]
fn sosein_api_url(api_base: &str, path: &str) -> Result<String, String> {
    let path = normalized_sosein_api_path(path)?;

    Ok(format!("{api_base}{path}"))
}

#[cfg(not(target_os = "ios"))]
fn compact_response_suffix(body: &str) -> String {
    let compact = body.split_whitespace().collect::<Vec<_>>().join(" ");

    if compact.is_empty() {
        String::new()
    } else {
        format!(": {}", compact.chars().take(240).collect::<String>())
    }
}

#[cfg(not(target_os = "ios"))]
fn oauth_query_value(target: &str, key: &str) -> Result<Option<String>, String> {
    let Some((_, query)) = target.split_once('?') else {
        return Ok(None);
    };

    for pair in query.split('&') {
        let (raw_key, raw_value) = pair.split_once('=').unwrap_or((pair, ""));

        if percent_decode_query_value(raw_key)? == key {
            return Ok(Some(percent_decode_query_value(raw_value)?));
        }
    }

    Ok(None)
}

#[cfg(not(target_os = "ios"))]
fn percent_encode_query_value(value: &str) -> String {
    let mut encoded = String::new();

    for byte in value.bytes() {
        match byte {
            b'A'..=b'Z' | b'a'..=b'z' | b'0'..=b'9' | b'-' | b'_' | b'.' | b'~' => {
                encoded.push(byte as char);
            }
            _ => encoded.push_str(&format!("%{byte:02X}")),
        }
    }

    encoded
}

#[cfg(not(target_os = "ios"))]
fn percent_decode_query_value(value: &str) -> Result<String, String> {
    let mut decoded = Vec::with_capacity(value.len());
    let bytes = value.as_bytes();
    let mut index = 0;

    while index < bytes.len() {
        match bytes[index] {
            b'+' => {
                decoded.push(b' ');
                index += 1;
            }
            b'%' if index + 2 < bytes.len() => {
                let hex = &value[index + 1..index + 3];
                let byte = u8::from_str_radix(hex, 16)
                    .map_err(|_| "Sosein Cloud login callback included invalid URL encoding.")?;

                decoded.push(byte);
                index += 3;
            }
            b'%' => {
                return Err(
                    "Sosein Cloud login callback included invalid URL encoding.".to_string()
                );
            }
            byte => {
                decoded.push(byte);
                index += 1;
            }
        }
    }

    String::from_utf8(decoded)
        .map_err(|_| "Sosein Cloud login callback included invalid UTF-8.".to_string())
}

