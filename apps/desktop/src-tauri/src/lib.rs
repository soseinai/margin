#[cfg(target_os = "macos")]
fn install_margin_cli() -> Result<PathBuf, String> {
    let app_bundle_path = current_macos_app_bundle_path()?;

    if let Some(app_bundle_path) = &app_bundle_path {
        if is_transient_macos_app_location(app_bundle_path) {
            return Err(format!(
                "Margin is running from {}. Drag Margin to Applications, quit this copy, reopen /Applications/Margin.app, then install the command again.",
                app_bundle_path.display()
            ));
        }
    }

    let destination = margin_cli_destination()?;
    let script = margin_cli_script(app_bundle_path.as_deref());

    write_executable_file(&destination, &script)?;

    Ok(destination)
}

