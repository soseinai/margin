fn parse_settings_toml(contents: &str) -> AppSettings {
    let mut settings = AppSettings::default();

    for line in contents.lines() {
        let line = line.trim();
        if line.is_empty() || line.starts_with('#') {
            continue;
        }

        let Some((key, value)) = line.split_once('=') else {
            continue;
        };

        let value = settings_value(value);

        match key.trim() {
            "theme" => settings.theme = normalized_theme(&value).to_string(),
            "local_user_name" | "localUserName" => {
                settings.local_user_name = normalized_local_user_name(&value);
            }
            "sosein_cloud_enabled" | "soseinCloudEnabled" => {
                if let Some(enabled) = settings_bool_value(&value) {
                    settings.sosein_cloud_enabled = enabled;
                }
            }
            _ => {}
        }
    }

    settings
}

fn settings_toml(settings: &AppSettings) -> String {
    format!(
        "theme = {}\nlocal_user_name = {}\nsosein_cloud_enabled = {}\n",
        toml_string_literal(normalized_theme(&settings.theme)),
        toml_string_literal(&normalized_local_user_name(&settings.local_user_name)),
        settings.sosein_cloud_enabled
    )
}

fn settings_bool_value(value: &str) -> Option<bool> {
    let value = value.trim().to_ascii_lowercase();
    match value.as_str() {
        "1" | "true" | "yes" | "on" => Some(true),
        "0" | "false" | "no" | "off" => Some(false),
        _ => None,
    }
}

