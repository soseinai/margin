            let insert_comment = MenuItem::with_id(
                app,
                "insert_comment",
                "Comment",
                true,
                Some("CmdOrCtrl+Alt+M"),
            )?;
            let insert_comment_separator = PredefinedMenuItem::separator(app)?;
